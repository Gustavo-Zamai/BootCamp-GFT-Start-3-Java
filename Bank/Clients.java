public class Clients {
    private String name;
    private String cpf;
    private int id;

    public String getName(){
        return name;
    }

    public void setName(){
        this.name = name;
    }

    public String getCpf(){
        return cpf;
    }

    public void setCpf(){
        this.cpf = cpf;
    }

    public int getId(){
        return id;
    }

    public void setId(){
        this.id = id;
    }

}
